const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

Deno.serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are PredictWell's AI Health Assistant — a knowledgeable, empathetic health advisor embedded in a behavioral health monitoring dashboard.

Your expertise covers:
- Sleep health (quality, duration, deep sleep, circadian rhythm, insomnia)
- Physical activity (steps, sedentary behavior, exercise recommendations)
- Mental health (stress, depression indicators, anxiety, burnout)
- Cardiovascular health (heart risk factors, lifestyle impacts)
- Nutrition and diet
- General wellness and preventive health
- Typing speed and cognitive performance indicators
- Voice stress analysis

Guidelines:
- Answer ALL health-related questions thoroughly and helpfully
- Keep responses concise but informative (2-4 paragraphs max)
- Use emoji sparingly for friendliness 🩺
- Always remind users you're an AI assistant, not a doctor, when giving specific medical advice
- If asked non-health questions, politely redirect to health topics
- Reference behavioral data patterns when relevant (sleep, steps, stress scores)
- Provide actionable recommendations when possible`,
          },
          ...messages,
        ],
        stream: true,
        max_tokens: 1024,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits in Settings." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service unavailable" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("health-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
